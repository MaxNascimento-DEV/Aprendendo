package Aulas;

public class Exc002 {

      private int number;
      private String name;
      private int years;

      public Exc002(int number, String name, int years) {
          this.number = number;
          this.name = name;
          this.years = years;
      }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public String getName(){
          return name;
    }

    public void setName(String name){
          this.name = name;
    }

    public int getYears(){
          return years;
    }

    public void setYears(int years) {
        this.years = years;
    }

}

